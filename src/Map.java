  /**
   * Return method that returns a certain room's map as a 2D array <br>
   * Updates: 4.0 - updated to get a room's map
   * 
   * @param level the level in which the map is in
   * @param room which room 
   * @return the correct map if the level and room exist or an empty map if the they don't exist
   */
  public static int[][] getMap (int level, int room)
  {
    if (level == 1)
      return getMap1 ()[room];
    else if (level == 2)
      return getMap2 ()[room];
    else if (level == 3)
      return getMap3 ()[room];
    return new int[0][0];
  }
  
  /**
   * Return method that returns the number of rooms in a level <br>
   * Updates: 4.0 - class created
   * 
   * @param level the level being checked
   * @return the number of rooms in the level if it exists
   */
  public static int getNumRooms (int level)
  {
    if (level == 1)
      return getMap1 ().length;
    else if (level == 2)
      return getMap2 ().length;
    else if (level == 3)
      return getMap3 ().length;
    return 0;
  }
  /**
   * Static return method that returns an ArrayList of all the Characters in a level read from a file <br>
   * Updates: 2.0 - changed the file being read from, added if statement to check if a character is important or not <br>
   *          3.0 - modified method to get the intro as well <br>
   *          4.0 - now reads and initializes all rooms, intros, and endings
   * 
   * <pre>
   * Local Variable Library
   * Name         Type             Description
   * numOfStages  int              The number of stages a level has
   * temp         String           Temporary String used to check dialogue before adding it to the Character
   * room         Room             Current room being created
   * mara         Mara             Current instance of Mara being created
   * reader       BufferedReader   Instance of BufferedReader used to read from a file
   * rooms        ArrayList&lt;Room&gt;  List of all the characters in a level
   * ch           Character        Current character (sometimes important) being created
   * </pre>
   * 
   * @param level the level used
   * @return an ArrayList of all the Room objects in a level
   */
  public static ArrayList<Room> initializeLevel (int level)
  {
    int numOfStages = 0;
    String temp;
    ArrayList<Room> rooms = new ArrayList<Room> ();
    for (int x = 0; x < getNumRooms (level); x++)
    {
      Room room = new Room (); //initializes new Room object
      try
      {
        BufferedReader reader = new BufferedReader (new FileReader ("text/Level " + level + " Room " + x + ".txt"));
        room = new Room (Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()));
        room.setMap (getMap (level, x));
        if (x == 0)
          numOfStages = Integer.parseInt (reader.readLine ());
      }
      catch (Exception e) {}
      for (int r = 0; r < room.getMap ().length; r++) //finds characters on the map and initializes them
      {
        for (int c = 0; c < room.getMap ()[0].length; c++)
        {
          if (room.getMap ()[r][c] == 3)
          {
            try
            {
              BufferedReader reader = new BufferedReader (new FileReader ("text/Level " + level + " " + x + " " + r + " " + c + ".txt"));
              temp = reader.readLine ();
              if (temp.charAt (0) == '*') //checks if character is important
              {
                ImportantCharacter ch = new ImportantCharacter (r, c, numOfStages, temp.substring (1));
                ch.setFacing (Integer.parseInt (reader.readLine ()));
                for (int y = 0; y < numOfStages; y++)
                {
                  temp = "";
                  while (!temp.equals ("end"))
                  {
                    temp = reader.readLine ();
                    ch.addDialogue (temp, y);
                  }
                }
                while (true)
                {
                  temp = reader.readLine ();
                  if (temp != null)
                    ch.addStage (Integer.parseInt (temp));
                  else
                    break;
                }
                room.addCharacter (ch);
              }
              else
              {
                Character ch = new Character (r, c, temp);
                ch.setFacing (Integer.parseInt (reader.readLine ()));
                while (!temp.equals ("end"))
                {
                  temp = reader.readLine ();
                  ch.addDialogue (temp);
                }
                room.addCharacter (ch);
              }
              reader.close ();
            }
            catch (Exception e) {}
          }
        }
      }
      if (x == 0) //adds intro, ending, and Mara to the first room's characters
      {
        try
        {
          BufferedReader reader = new BufferedReader (new FileReader("text/Level " + level + " Opening.txt"));
          Character ch = new Character (0, 0, "Opening");
          temp = "";
          while (!temp.equals ("end"))
          {
            temp = reader.readLine ();
            ch.addDialogue (temp);
          }
          reader.close ();
          room.addCharacter (ch);
          reader = new BufferedReader (new FileReader("text/Level " + level + " Ending.txt"));
          ch = new Character (0, 0, "Ending");
          temp = "";
          while (!temp.equals ("end"))
          {
            temp = reader.readLine ();
            ch.addDialogue (temp);
          }
          reader.close ();
          room.addCharacter (ch);
          reader = new BufferedReader (new FileReader("text/Level " + level + " Mara.txt"));
          temp = reader.readLine ();
          Mara mara = new Mara (Integer.parseInt (reader.readLine ()), Integer.parseInt (reader.readLine ()), temp);
          mara.setStage (Integer.parseInt (reader.readLine ()));
          while (!temp.equals ("end"))
          {
            temp = reader.readLine ();
            mara.addDialogue (temp);
          }
          reader.close ();
          room.addCharacter (mara);
        }
        catch (Exception e) {}
      }
      rooms.add (room);
    }
    if (level == 2) //level 2 starts in room 6
      rooms.get (6).addCharacter (rooms.get (0).getCharacters ().get (rooms.get (0).getCharacters ().size () - 3));
    return rooms;
  }
